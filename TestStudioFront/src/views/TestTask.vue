<template>
  <!-- <v-data-table :headers="headers" :items="TestTasks" sort-by="taskid" class="elevation-1">-->
  <v-data-table :headers="headers" :items="TestTasks" class="elevation-1">
    <template v-slot:top>
      <v-toolbar flat>
        <v-toolbar-title>测试任务概览</v-toolbar-title>
        <v-divider class="mx-4" inset vertical></v-divider>
        <v-spacer></v-spacer>
        <v-dialog v-model="dialog" max-width="500px">
          <template v-slot:activator="{ on, attrs }">
            <v-btn color="primary" dark class="mb-2" v-bind="attrs" v-on="on">
              New Item
            </v-btn>
          </template>
          <v-card>
            <v-card-title>
              <span class="text-h5">{{ formTitle }}</span>
            </v-card-title>

            <v-card-text>
              <v-container>
                <v-row>
                  <v-col cols="12" sm="6" md="4">
                    <v-text-field v-model="editedItem.name" label="TestTask name"></v-text-field>
                  </v-col>
                  <v-col cols="12" sm="6" md="4">
                    <v-text-field v-model="editedItem.TaskId" label="TaskId"></v-text-field>
                  </v-col>
                  <v-col cols="12" sm="6" md="4">
                    <v-text-field v-model="editedItem.createtime" label="CreateTime"></v-text-field>
                  </v-col>
                  <v-col cols="12" sm="6" md="4">
                    <v-text-field v-model="editedItem.caseidlist" label="CaseIdList"></v-text-field>
                  </v-col>
                  <v-col cols="12" sm="6" md="4">
                    <v-text-field v-model="editedItem.schedule" label="Schedule"></v-text-field>
                  </v-col>
                </v-row>
              </v-container>
            </v-card-text>

            <v-card-actions>
              <v-spacer></v-spacer>
              <v-btn color="blue darken-1" text @click="close">
                Cancel
              </v-btn>
              <v-btn color="blue darken-1" text @click="save">
                Save
              </v-btn>
            </v-card-actions>
          </v-card>
        </v-dialog>
        <v-dialog v-model="dialogDelete" max-width="500px">
          <v-card>
            <v-card-title class="text-h5">Are you sure you want to delete this item?</v-card-title>
            <v-card-actions>
              <v-spacer></v-spacer>
              <v-btn color="blue darken-1" text @click="closeDelete">Cancel</v-btn>
              <v-btn color="blue darken-1" text @click="deleteItemConfirm">OK</v-btn>
              <v-spacer></v-spacer>
            </v-card-actions>
          </v-card>
        </v-dialog>
      </v-toolbar>
    </template>
    <template v-slot:[`item.actions`]="{ item }">
      <v-icon small class="mr-2" @click="editItem(item)">
        mdi-pencil
      </v-icon>
      <v-icon small @click="deleteItem(item)">
        mdi-delete
      </v-icon>
    </template>
    <template v-slot:no-data>
      <v-btn color="primary" @click="initialize">
        Reset
      </v-btn>
    </template>
  </v-data-table>
</template>

<script>
export default {
  name: 'TestTask',
  data: () => ({
    dialog: false,
    dialogDelete: false,
    headers: [
      {
        text: 'TestTask',
        align: 'start',
        sortable: false,
        value: 'name',
      },
      { text: 'TaskId', value: 'taskid' },
      { text: 'CreateTime', value: 'createtime' },
      { text: 'CaseIdList', value: 'caseidlist' },
      { text: 'Schedule', value: 'schedule' },
      { text: 'Actions', value: 'actions', sortable: false },
    ],
    TestTasks: [],
    editedIndex: -1,
    editedItem: {
      name: '',
      taskid: 0,
      createtime: 0,
      caseidlist: 0,
      schedule: 0,
    },
    defaultItem: {
      name: '',
      taskid: 0,
      createtime: 0,
      caseidlist: 0,
      schedule: 0,
    },
  }),

  computed: {
    formTitle() {
      return this.editedIndex === -1 ? 'New Item' : 'Edit Item'
    },
  },

  watch: {
    dialog(val) {
      val || this.close()
    },
    dialogDelete(val) {
      val || this.closeDelete()
    },
  },

  created() {
    this.initialize()
  },

  methods: {

    initialize() {
      this.$api.testtask.getTestTask().then((result) => {
        console.log("getTestTask", result)
        this.TestTasks = result.data
      }).catch((err) => {
        console.log(err)
      });
    },

    // initialize() {
    //   this.TestTasks = [
    //     {
    //       name: 'Frozen Yogurt',
    //       taskid: 159,
    //       createtime: 6.0,
    //       caseidlist: 24,
    //       schedule: 4.0,
    //     },
    //     {
    //       name: 'Ice cream sandwich',
    //       taskid: 237,
    //       createtime: 9.0,
    //       caseidlist: 37,
    //       schedule: 4.3,
    //     },
    //   ]
    // },

    editItem(item) {
      this.editedIndex = this.TestTasks.indexOf(item)
      this.editedItem = Object.assign({}, item)
      this.dialog = true
    },

    deleteItem(item) {
      this.editedIndex = this.TestTasks.indexOf(item)
      this.editedItem = Object.assign({}, item)
      this.dialogDelete = true
    },

    deleteItemConfirm() {
      this.TestTasks.splice(this.editedIndex, 1)
      this.closeDelete()
    },

    close() {
      this.dialog = false
      this.$nextTick(() => {
        this.editedItem = Object.assign({}, this.defaultItem)
        this.editedIndex = -1
      })
    },

    closeDelete() {
      this.dialogDelete = false
      this.$nextTick(() => {
        this.editedItem = Object.assign({}, this.defaultItem)
        this.editedIndex = -1
      })
    },

    save() {
      if (this.editedIndex > -1) {
        Object.assign(this.TestTasks[this.editedIndex], this.editedItem)
      } else {
        this.TestTasks.push(this.editedItem)
      }
      this.close()
    },
  },
  mounted() {
    this.timer = setInterval(this.initialize, 50000);
  },
}
</script>
